bus={
    init:()=>{
        $('.cus_submit_strict').click((e)=>{
            e.preventDefault()
            bus.submitStrictForm()
        })
        $('.cus_submit').click((e)=>{
            e.preventDefault()
            bus.submitForm()
        })
    },
    submitStrictForm:()=>{
        bp();
        if($('#bus-form')[0].checkValidity()){
            setTimeout(()=>{
                $('.bus-form').submit();
            },3000)
        }else{
            notifyError('Error','Kindly fill out the form correctly');
            setTimeout(()=>{
                $.unblockUI()
            },3000)
        }    
    },
    submitForm:()=>{
        bp();
        setTimeout(()=>{
            $('.bus-form').submit();
        },3000)
    }

}
$(()=>{
    bus.init()
});